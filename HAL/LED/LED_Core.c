/******************************************************************************
* \Syntax          : void LED_Init(uint8 LED_num)
* \Description     : initialize the direction of pin connected to led
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void LED_Init(uint8 LED_num)
{
	SET_BIT(DIO_PORTC->DDR,LED_num);
}

/******************************************************************************
* \Syntax          : Std_ReturnType LED_ON(uint8 LED_num)
* \Description     : turn on the led
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void LED_ON(uint8 LED_num)
{
	SET_BIT(DIO_PORTC->PORT,LED_num);
}

/******************************************************************************
* \Syntax          : void LED_OFF(uint8 LED_num)
* \Description     : turn off the led
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void LED_OFF(uint8 LED_num)
{
	CLR_BIT(DIO_PORTC->PORT,LED_num);
}

/******************************************************************************
* \Syntax          : void LED_TGL(uint8 LED_num)
* \Description     : toggle the led
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : parameterName   Parameter Describtion
* \Parameters (out): None
* \Return value:   : Std_ReturnType  E_OK
*                                    E_NOT_OK
*******************************************************************************/
void LED_TGL(uint8 LED_num)
{
	TGL_BIT(DIO_PORTC->PORT,LED_num);
}



