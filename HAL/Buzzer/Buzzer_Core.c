void buzzer_init(void)
{
	SET_BIT(DIO_PORTC->DDR,5);
}
void buzzer_ON(void)
{
	SET_BIT(DIO_PORTC->PORT,5);
	_delay_ms(5000);
	CLR_BIT(DIO_PORTC->PORT,5);
	_delay_ms(5000);
}

